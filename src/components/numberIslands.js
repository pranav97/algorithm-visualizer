/**
 * @param {character[][]} grid
 * @return {number}
 */

left = (grid, loc) => {
    if (loc[1] - 1 >= 0) {
      return [loc[0], loc[1] - 1];
    }
}

right = (grid, loc) => {
    if (loc[1] + 1 < grid[0].length) {
      return [loc[0], loc[1] + 1];
    }

}

up = (grid, loc) => {
    if (loc[0] - 1 >= 0) {
        return [loc[0]-1, loc[1]];
    }
}

down = (grid, loc) => {
    if (loc[0] + 1 < grid.length) {
        return [loc[0]+1, loc[1]];
    }
}

checkLand = (grid, neighbor, queue) => {
    if ( neighbor != null && grid[neighbor[0]][neighbor[1]] === '1') {
        grid[neighbor[0]][neighbor[1]] = 0
        queue.push(neighbor);
    }
    return queue;
}

bfs = (grid, startRow, startCol) => {
    var queue = [[startRow,startCol]]
    
    while (queue.length > 0) {
        var cur = queue.shift();
        var r = right(grid, cur);
        var l = left(grid, cur)
        var u =  up(grid, cur);
        var d =  down(grid, cur);
        
        queue = checkLand(grid, r, queue);
        queue = checkLand(grid, l, queue);
        queue = checkLand(grid, u, queue);
        queue = checkLand(grid, d, queue);
    }
}

numIslands = (grid) => {
    var countNumIslands = 0
    for(i = 0; i < grid.length; i++) {
        for(j = 0; j < grid[i].length; j++) {
            if (grid[i][j] == 1) {
                bfs(grid, i, j);
                countNumIslands++;
            }
        }
    }
    return countNumIslands;
};


