/**
 * @param {character[][]} grid
 * @return {number}
 */

export function left(grid, loc) {
    if (loc[1] - 1 >= 0) {
      return [loc[0], loc[1] - 1];
    }
}

export function right(grid, loc) {
    if (loc[1] + 1 < grid[0].length) {
      return [loc[0], loc[1] + 1];
    }

}

export function up(grid, loc){
    if (loc[0] - 1 >= 0) {
        return [loc[0]-1, loc[1]];
    }
}

export function down(grid, loc) {
    if (loc[0] + 1 < grid.length) {
        return [loc[0]+1, loc[1]];
    }
}

export function checkLand(grid, neighbor, queue) {
    if ( neighbor != null && grid[neighbor[0]][neighbor[1]] === '1') {
        grid[neighbor[0]][neighbor[1]] = 0
        queue.push(neighbor);
    }
    return queue;
}

export function bfs(grid, startRow, startCol){
// bfs = (grid, startRow, startCol) => {
    var queue = [[startRow,startCol]]
    
    while (queue.length > 0) {

        // console.log("queue", queue);
        var cur = queue.shift();
        var r = right(grid, cur);
        var l = left(grid, cur)
        var u =  up(grid, cur);
        var d =  down(grid, cur);
        
        queue = checkLand(grid, r, queue);
        queue = checkLand(grid, l, queue);
        queue = checkLand(grid, u, queue);
        queue = checkLand(grid, d, queue);
    }
}



